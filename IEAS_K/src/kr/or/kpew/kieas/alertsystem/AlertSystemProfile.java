package kr.or.kpew.kieas.alertsystem;

public class AlertSystemProfile {
	private String alertSystemId;
	private String alertSystemType;
	private String geoCode;
	public String getAlertSystemId() {
		return alertSystemId;
	}
	public void setAlertSystemId(String alertSystemId) {
		this.alertSystemId = alertSystemId;
	}
	public String getAlertSystemType() {
		return alertSystemType;
	}
	public void setAlertSystemType(String alertSystemType) {
		this.alertSystemType = alertSystemType;
	}
	public String getGeoCode() {
		return geoCode;
	}
	public void setGeoCode(String geoCode) {
		this.geoCode = geoCode;
	}


}
